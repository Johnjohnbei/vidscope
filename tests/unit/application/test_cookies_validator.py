"""Tests for the Netscape cookies file validator."""

from __future__ import annotations

from pathlib import Path

import pytest

from vidscope.application.cookies_validator import (
    CookiesValidation,
    validate_cookies_file,
)


def _write(path: Path, content: str) -> Path:
    path.write_text(content, encoding="utf-8")
    return path


# ---------------------------------------------------------------------------
# Happy path
# ---------------------------------------------------------------------------


class TestValidCookies:
    def test_minimal_valid_file(self, tmp_path: Path) -> None:
        cookies = _write(
            tmp_path / "cookies.txt",
            "# Netscape HTTP Cookie File\n"
            ".instagram.com\tTRUE\t/\tTRUE\t1893456000\tsessionid\tabc123\n",
        )
        result = validate_cookies_file(cookies)
        assert result.ok is True
        assert result.entries_count == 1
        assert result.reason == ""

    def test_multiple_valid_rows(self, tmp_path: Path) -> None:
        cookies = _write(
            tmp_path / "cookies.txt",
            "# Netscape HTTP Cookie File\n"
            ".instagram.com\tTRUE\t/\tTRUE\t1893456000\tsessionid\tabc\n"
            ".instagram.com\tTRUE\t/\tFALSE\t1893456000\tcsrftoken\tdef\n"
            ".instagram.com\tTRUE\t/\tTRUE\t1893456000\tds_user_id\t12345\n",
        )
        result = validate_cookies_file(cookies)
        assert result.ok is True
        assert result.entries_count == 3

    def test_no_header_still_valid(self, tmp_path: Path) -> None:
        # Some browser extensions skip the header altogether
        cookies = _write(
            tmp_path / "cookies.txt",
            ".instagram.com\tTRUE\t/\tTRUE\t1893456000\tsessionid\tabc\n",
        )
        result = validate_cookies_file(cookies)
        assert result.ok is True
        assert result.entries_count == 1

    def test_mixed_comments_and_rows(self, tmp_path: Path) -> None:
        cookies = _write(
            tmp_path / "cookies.txt",
            "# Netscape HTTP Cookie File\n"
            "# Generated by some browser extension\n"
            "\n"
            ".instagram.com\tTRUE\t/\tTRUE\t1893456000\tsessionid\tabc\n"
            "# A comment between rows\n"
            ".instagram.com\tTRUE\t/\tFALSE\t1893456000\tcsrftoken\tdef\n",
        )
        result = validate_cookies_file(cookies)
        assert result.ok is True
        assert result.entries_count == 2

    def test_empty_value_field_is_valid(self, tmp_path: Path) -> None:
        # Some cookies have an empty value (it's a valid case)
        cookies = _write(
            tmp_path / "cookies.txt",
            ".example.com\tTRUE\t/\tFALSE\t0\tflag\t\n",
        )
        result = validate_cookies_file(cookies)
        assert result.ok is True
        assert result.entries_count == 1

    def test_carriage_returns_handled(self, tmp_path: Path) -> None:
        # Windows-exported files may have CRLF line endings
        cookies = _write(
            tmp_path / "cookies.txt",
            "# Netscape HTTP Cookie File\r\n"
            ".example.com\tTRUE\t/\tTRUE\t0\tname\tvalue\r\n",
        )
        result = validate_cookies_file(cookies)
        assert result.ok is True
        assert result.entries_count == 1


