class TestInvalidCookies:
    def test_file_does_not_exist(self, tmp_path: Path) -> None:
        result = validate_cookies_file(tmp_path / "missing.txt")
        assert result.ok is False
        assert "does not exist" in result.reason
        assert result.entries_count == 0

    def test_path_is_directory(self, tmp_path: Path) -> None:
        result = validate_cookies_file(tmp_path)
        assert result.ok is False
        assert "not a regular file" in result.reason

    def test_empty_file(self, tmp_path: Path) -> None:
        cookies = _write(tmp_path / "cookies.txt", "")
        result = validate_cookies_file(cookies)
        assert result.ok is False
        assert "empty" in result.reason

    def test_only_whitespace(self, tmp_path: Path) -> None:
        cookies = _write(tmp_path / "cookies.txt", "  \n\n  \n")
        result = validate_cookies_file(cookies)
        assert result.ok is False
        assert "empty" in result.reason

    def test_only_comments(self, tmp_path: Path) -> None:
        cookies = _write(
            tmp_path / "cookies.txt",
            "# Netscape HTTP Cookie File\n"
            "# Just comments, no data\n",
        )
        result = validate_cookies_file(cookies)
        assert result.ok is False
        assert "no cookie rows" in result.reason

    def test_malformed_rows_only(self, tmp_path: Path) -> None:
        cookies = _write(
            tmp_path / "cookies.txt",
            "# Netscape HTTP Cookie File\n"
            "this is not a valid cookie line\n"
            "neither is this one\n",
        )
        result = validate_cookies_file(cookies)
        assert result.ok is False
        assert "malformed" in result.reason

    def test_wrong_column_count(self, tmp_path: Path) -> None:
        # Only 5 columns instead of 7
        cookies = _write(
            tmp_path / "cookies.txt",
            ".example.com\tTRUE\t/\tTRUE\t0\n",
        )
        result = validate_cookies_file(cookies)
        assert result.ok is False
        assert "malformed" in result.reason

    def test_empty_domain_column(self, tmp_path: Path) -> None:
        cookies = _write(
            tmp_path / "cookies.txt",
            "\tTRUE\t/\tTRUE\t0\tname\tvalue\n",
        )
        result = validate_cookies_file(cookies)
        assert result.ok is False
        assert "malformed" in result.reason


